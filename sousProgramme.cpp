#include "sousProgramme.h"
#include <iostream>
using namespace std;

// Procédure pour afficher 10 nombres
void affichage10Nombres(int nbDepart) {
    for (int i = 0; i < 10; i++) {  // i commence à 0 pour afficher les 10 nombres à partir de nbDepart
        cout << nbDepart + i << endl;
    }
}

